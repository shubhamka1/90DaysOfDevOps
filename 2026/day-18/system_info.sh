#!/bin/bash
set -euo pipefail

host(){
	uname -a
}

up_time(){
	uptime -p
}

disk_usage(){
	df -h | head -n 5
}

mem(){

	free -h
}

topproc(){
	ps -aux --sort=-%cpu | head -n 6
 
}

main(){
	host
	echo "--------------------------------------------------------------------------"
	up_time
	echo "--------------------------------------------------------------------------"
	disk_usage
	echo "--------------------------------------------------------------------------"
	mem
	echo "--------------------------------------------------------------------------"
	topproc
}


main
